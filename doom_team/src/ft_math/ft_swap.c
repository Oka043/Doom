#include "player_stable.h"

void	ft_swap_d(double *x, double *y)
{
	double	c;

	c = *x;
	*x = *y;
	*y = c;
}

void	ft_swap_f(float *x, float *y)
{
	float	c;

	c = *x;
	*x = *y;
	*y = c;
}
